			<p>If you are a class with a lot of young families, we suggest doing
				a picnic in the park. The event is family friendly and keeps to a
				very low budget.</p>
			<p>If your class is conflicted on a family friendly vs. adults only
				event, look into places that have a family friendly area and a bar,
				like a bowling alley or brewery.</p>
			<p>Before suggesting doing a dinner event for your reunion, be sure to
				research the upfront cost to the planning committee first. Talk it 
				over with your committee about the feasibility.</p>
