				<hr class="goldbar" width="75%">
				<p class="dInfo">Talking to your fellow classmates you are still in
					contact with, you will want to create a master list of the mailing
					addresses of as many of your classmates as possible. Utilize a
					phone tree and those old address books and collect the current
					information of your classmates.</p>
				<p class="dInfo">This is also the time to be hearing opinions from
					your classmates and finding out who would be interested in helping.
					Be sure to make note of all those who make any offers, you never
					know when you might need to cash in on one!</p>
			</div>
