				<h3 class="dtitle">Make Polls</h3>
				<hr class="goldbar" width="75%">
				<p class="dInfo">Now that you have everyone together in your
					Facebook group, see what everyone would like to do by create polls.
					When creating a post in the group, select "poll" from the post
					options. Remember, the more you ask, the easier the decision making
					will be for you.</p>
				<p class="dInfo">Suggestions on questions to gather: who can help
					take action, preferred time of the week (Friday evening, Sunday
					afternoon), location (St. Johns, Portland city limits), event type
					(family friendly, adults only), price range, etc.</p>
			</div>
			<div class="blockD col-lg-4 col-md-4 col-sm-5 col-xs-5 justify-content-center text-center">
				<h3 class="dtitle">Form a Committee</h3>
				<hr class="goldbar" width="75%">
				<p class="dInfo">Gather the people who were interested in helping
					when you collected everyone's information and form a planning
					committee. Your committee will make all of your decisions for the
					reunion. How you structure you committee is up to you, but in
					general you will want a committee chair, a secretary, and
					communications chair.</p>
				<p class="dInfo">Having a single communications chair ensures that
					communication is kept consistent, and having a secretary keeps
					everyone on the same page. All additional members should have very
					specific duties assigned by the committee chair.</p>
			</div>
		</div>
