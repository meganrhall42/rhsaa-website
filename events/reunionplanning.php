				<hr class="goldbar" width="75%">
				<p class="dInfo">This is our reccomended route if it is possible for
					your class. If you and your classmates are on Facebook, you will
					have a pretty easy time collecting your classmates! Even if you
					aren't Facebook friends with everyone, you know someone who knows
					someone else and so on.</p>
				<p class="dInfo">Create a Facebook group (not page) named "RHS Class
					of XXXX" and add everyone you are friends with. Allow
					non-moderators to add people via the group settings and make a post
					in the group about adding your other classmates. In less than a
					week you will have a huge portion of your class in one spot!</p>
			</div>
			<div class="blockD col-lg-4 col-md-4 col-sm-5 col-xs-5 justify-content-center text-center">
