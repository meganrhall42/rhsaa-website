		<div class="text-center bylawsection">
			<h4>Article II</h4>
			<hr width="25%">
			<h5>Purpose and Nondiscrimination</h5>
			</br>
			<h6>2.1 Purpose.</h6>
			<p>2.1.1. The Association is organized and operated exclusively for
				charitable and educational purposes within the meaning of Section
				501 [c] [3] of the Internal Revenue Code of 1986, as amended, herein
				referred to as the "code".</p>
			<p>2.1.2. The specific and primary purpose of the Association is to
				support Roosevelt High School, its students and its community,
				including alumni.</p>
			<p>2.1.3. Notwithstanding any other provision of these Bylaws, the
				Association will not engage in any activity or exercise any power
				that is not in furtherance of a charitable or education purpose, and
				will not carry on any activity not permitted to be carried on (i) by
				an Association exempt from federal income taxation under Section 501
				[c] [3] of the Code, or (ii) by a corporation, contributions to
				which are deductible under Section 170 [c] [2].</p>
			<p>2.1.4. No part of the activities of this Association will consist
				of carrying on propaganda or otherwise attempting to influence
				legislation (except as otherwise provided in Section 501 [h]), and
				the Association will not participate in or intervene in (including
				the publishing and distributing of statements) any political
				campaign on behalf of (or in opposition to) any candidate for public
				office.</p>
			<h6>2.2 Nondiscrimination.</h6>
			<p>2.2.1 All Directors and Officers of the Association shall be
				chosen and accepted without discrimination on the basis of race,
				religions, national origin, ethnicity, sex, sexual orientation,
				physical challenges, age, or any other legally barred basis.</p>
		</div>
		<div class="text-center bylawsection">
			<h4>Article III</h4>
			<hr width="25%">
			<h5>Membership</h5>
			</br>
			<p>3.1.1 Voting Rights. The Association has no "members" as that term
				is defined in ORS 65.001. The Board of Directors might, from time to
				time, specify titles or designations applicable to friends and
				supporters of the Association that might use the term "member." Such
				designation shall not create in any person rights of a "member" as
				defined in Oregon law, however each Director has the right to vote
				on disposition of all or substantially all of the assets of the
				Association or on a merger or dissolution.</p>
		</div>
		<div class="text-center bylawsection">
			<h4>Article IV</h4>
			<hr width="25%">
			<h5>Board of Directors</h5>
			</br>
			<h6>4.1 General Powers</h6>
			<p>Except as provided in the Articles of Incorporation or these
				Bylaws, all corporate powers, affairs, and finances of the
				Association shall be managed under the authority of the Board of
				Directors.</p>
			<h6>4.2 Number and Qualifications.</h6>
			<p>4.2.1. Directors of the Board shall be:</p>
			<p>4.2.2. Anyone who has attended Roosevelt High School, whether
				graduating or not;</p>
			<p>4.2.3. Former members of the administration, faculty and staff of
				the school;</p>
			<p>4.2.4. Supporters of the school recommended to the Board of the
				Association.</p>
			<p>4.2.5. The Board shall include a minimum of twenty-two (22) and a
				maximum of thirty (30) Directors elected by the Board of Directors.</p>
			<p>4.2.6. Board membership is intended that the Directors represent
				each decade of students from Roosevelt High School beginning with
				the 1940's (when such a representative is available and able to
				serve).</p>
			<p>4.2.7. The principal or his/her designee from the current school
				administration shall be an ex officio nonvoting member of the Board.</p>
			<p>4.2.8. Upon recommendation by the Nominating Committee, the Board
				can also designate other ex officio nonvoting members. All ex
				officio members of the Board are nonvoting, unless the Board
				specifically authorizes voting privileges.</p>
			<h6>4.3 Term.</h6>
			<p>Except as specified in Sections 4.2.7 and 4.2.8, Directors shall
				serve three-year terms, selected in such a way as to allow
				approximately one-third of them to be replaced each year. Each
				Director's term commences on July 1 and terminates on June 30 of the
				applicable year; however, Directors appointed to serve unexpired
				terms shall take office upon appointment.</p>
			<p>4.3.2. There shall be no limit on the number of successive terms
				that Officers and Board members of the Association may serve.</p>
			<p>4.3.3. Upon recommendation of the Nominating Committee, the Board
				can appoint a member of the Association to fill the unexpired term
				of any voting Board member elected by the Board who is unable to
				complete his or her term of office.</p>
			<h6>4.4 Nomination and Election.</h6>
			<p>4.4.1 Nominations for Directors shall be made by the Nominating
				Committee. At least thirty days prior to the Annual meeting, the
				nominations of the Nominating Committee shall be mailed to the Board
				of Directors. Nominations for Directors can also be made by one or
				more Directors at least ten days prior to the Annual Meeting. Such
				additional nominations shall also be mailed to the Board of
				Directors upon their receipt by the Nominating Committee. The Board
				shall consider each of the recommended nominees, and nominees
				received from Directors, and shall elect Directors at the Annual
				Meeting by written ballot.</p>
			<h6>4.5 Conflict of Interest.</h6>
			<p>4.5.1. If a Director knows or reasonably believes that a conflict
				of interest transaction in which s/he has a direct or indirect
				interest is to be discussed or voted on at a meeting, the Director
				will, either prior to or at the meeting, disclose fully to the Board
				of Directors or to a Committee of the Board of Directors the
				material facts of the transaction and the Director's interest in the
				transaction</p>
			<p>4.5.2. A "conflict of interest" transaction is a transaction with
				the Association in which a Director has a direct or indirect
				interest (whether financial or otherwise or whether actual or
				potential).</p>
			<p>4.5.3. A Director has an "indirect interest" in a transaction if
				(i) a family member of the Director is a party to the transaction,
				(ii) another entity in which the Director has a material interest is
				a party to the transaction, or (iii) another entity of which the
				Director is a director, officer or trustee is party to the
				transaction, and the transaction is or should be considered by the
				Board of Directors.</p>
			<p>4.5.4 A "family member" of an individual includes the individual's
				brothers and sisters (whether by whole blood or half blood), spouse,
				ancestors, lineal descendants and in-laws of any of the foregoing. A
				legally adopted child of an individual will be treated as a child of
				such individual by blood.</p>
			<p>4.5.5. A conflict of interest transaction can be approved by the
				vote of the Board of Directors or a committee of the Board of
				Directors if the material facts of the transaction and the
				Director's interest in the transaction are disclosed fully or known
				to the Board of Directors or committee of the Board of Directors. A
				conflict of interest transaction is authorized, approved and
				ratified if it receives the affirmative vote of a majority of the
				Directors who have no direct or indirect interest in the
				transaction; however, a conflict of interest transaction may not be
				authorized, approved or ratified by fewer than two Directors. If a
				majority of a quorum of Directors, who have no direct or indirect
				interest in the transaction, votes to authorize, approve or ratify
				the transaction, action can be taken under this Section 4.5.5. The
				presence of, or vote cast by, a Director with a direct or indirect
				interest in the transaction will not affect the validity of any
				action taken if the transaction is otherwise approved as provided in
				this Section. 4.5.5.</p>
			<p>4.5.6. A conflict of interest transaction is not voidable and may
				not be the basis for imposing liability on a Director if the
				transaction was entered into or is approved as provided in Section
				4.5.5.</p>
			<h6>4.6 Liability.</h6>
			<p>4.6.1 No Director will be personally liable to the Association or
				the members, if any, for monetary damages for conduct as a Director;
				however, this Section 4.6 will not eliminate or limit the liability
				of a Director for (I) any act or omission occurring prior to the
				effective date of these Bylaws, (ii) any breach of the Director's
				duty of loyalty to the corporation, (iii) acts or omissions not in
				good faith which involve intentional misconduct or a knowing
				violation of law, (iv) any unlawful distribution, (v) any
				transaction from which the Director derived an improper personal
				benefit, and (vi) any act or omission in violation of ORS 65.361 or
				ORS 65.367.</p>
