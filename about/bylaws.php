			<h4>Article I</h4>
			<hr width="25%">
			<h5>Name</h5>
			</br>
			<p>This association is a nonprofit public benefit corporation
				incorporated under laws of the State of Oregon and shall be known as
				the Roosevelt High School Alumni Association, herein referred to as
				the "Association."</p>
