			<h4>Article V</h4>
			<hr width="25%">
			<h5>Meetings of the Board of Directors</h5>
			</br>
			<h6>5.1 Frequency.</h6>
			<p>5.1.1 The Board of Directors shall meet at least three (3) times a
				year</p>
			<h6>5.2 Annual Meeting.</h6>
			<p>5.2.1 The spring Board Meeting will be the Annual Meeting of the
				Board.</p>
			<h6>5.3 Special Meeting.</h6>
			<p>5.3.1 Special meetings of the Board shall be called by the
				President, or upon his or her failure to do so, by the Secretary
				when requested in writing by at least twenty (20) percent of the
				voting members of the Board. Notice of special meetings shall be
				provided to each Director at least ten (10) days before the meeting.</p>
			<h6>5.4 Quorum.</h6>
			<p>5.4.1 A majority of the members of the Board constitutes a quorum
				and is necessary to transact business.</p>
			<h6>5.5 Voting Requirements.</h6>
			<p>5.5.1 A simple majority vote of the quorum at any Board meeting
				shall decide all questions except removal of Directors and Officers,
				Amendments to the Bylaws, and Articles of Incorporation which shall
				require a two-thirds majority vote of the quorum of Directors</p>
			<h6>5.6 Mail, Telephone, Fax, Electronic Voting.</h6>
			<p>5.6.1 In the event the Executive Committee determines that a
				matter needs resolution by the Board prior to a Board Meeting, the
				Executive Committee can authorize submission of the matter to the
				Board for vote by use of mail, telephone, fax, email or other
				electronic means. In such a case, however, a majority vote of all
				voting Directors is necessary for adoption. Removal of Directors and
				Officers and amendments to the Bylaws and Articles of Incorporation
				shall not occur under this Section 5.6.</p>
		</div>
		<div class="text-center bylawsection">
			<h4>Article VI</h4>
			<hr width="25%">
			<h5>Officers</h5>
			</br>
			<h6>6.1 Responsibilities.</h6>
			<p>6.1.1 All Officers are responsible to the Board.</p>
			<h6>6.2 Officers.</h6>
			<p>6.2.1 The Officers of the Association are the Past President,
				President, Vice President, Treasurer and Secretary</p>
			<h6>6.3 Qualifications and Term of Office.</h6>
			<p>6.3.1 All Officers shall be members of the Association who qualify
				for membership under Section 3.1. The term of an office shall be two
				years, commencing July 1 and concluding on June 30.</p>
			<h6>6.4 Nomination and Election.</h6>
			<p>6.4.1 Nominations for Officers shall be made by the Nominating
				Committee. At least thirty (30) days prior to the Annual Meeting,
				the nominations of the Nominating Committee shall be mailed to the
				Board of Directors. Nominations for Officers can also be made by one
				or more Directors at least ten (10) days prior to the Annual
				Meeting. Such additional nominations shall also be mailed to the
				Board of Directors upon their receipt by the Nominating Committee.
				The Board shall consider each of the recommended nominees, and
				nominees received from the Directors, and shall elect Officers at
				the Annual Meeting by written ballot.</p>
			<h6>6.5 President.</h6>
			<p>6.5.1 The President shall: preside at all meetings of the Board,
				chair the Executive Committee, serve on the Nominating Committee,
				appoint committee Chairs in consultation with the Vice President and
				the Past President, subject to approval by the Executive Committee;
				act as the Association's Official Representative; have the option of
				serving as a member of all committees of the Board; and perform such
				other duties which may be assigned by the Board or which usually
				pertain to said office.</p>
			<h6>6.6 Past President.</h6>
			<p>6.6.1 The Past President shall: serve on the Executive Committee
				and the Nominating Committee; and, can serve on other committees as
				so appointed by the President.</p>
			<h6>6.7 Vice President.</h6>
			<p>6.7.1 The Vice President shall: serve on the Executive Committee
				and such other committees as assigned by the President.</p>
			<h6>6.8 Treasurer.</h6>
			<p>6.8.1 The Treasurer shall: serve on the Budget and Finance
				Committee; report and make recommendations to the Board and
				Executive Committee on Association finances and investments; and in
				general, perform all duties incident to the office of Treasurer. Any
				investment policies for funds and securities of the Association
				shall be approved by the Board and shall be developed and
				implemented by the Treasurer and the Budget and Finance Committee.</p>
			<h6>6.9 Secretary.</h6>
			<p>6.9.1 The Secretary shall: serve as Secretary to the Board and the
				Executive Committee, keeping all records thereof; and, in general,
				perform all duties incident to the office of Secretary. The
				Secretary shall also serve on the Nominating Committee.</p>
		</div>
		<div class="text-center bylawsection">
			<h4>Article VII</h4>
			<hr width="25%">
			<h5>Vacancies, Resignation and Removal of Officers and Directors</h5>
			</br>
			<h6>7.1 Resignation.</h6>
			<p>7.1.1 Any Officer or Director can resign at any time by delivering
				written notice to the President, or by giving oral or written notice
				at any meeting of the Board. Any such resignation shall take effect
				at the time specified therein, or, if the time is not specified,
				upon delivery thereof, and unless otherwise specified therein, the
				acceptance of such resignation shall not be necessary to make it
				effective.</p>
			<h6>7.2 Removal.</h6>
			<p>7.2.1 Any Officer or Director elected or appointed by the Board
				may be removed from office by the Board, with or without cause, by
				the affirmative vote of twothirds of the Board. Prior to any action
				being taken by the Board to remove an Officer or Director from
				office, the Officer or Director must be given at least ten (10) days
				prior notice of the proposed action and the date of the meeting at
				which the proposed action will be voted upon.</p>
			<h6>7.3 Unexpired Term.</h6>
			<p>7.3.1 A vacancy in any office created by the death, resignation,
				removal, disqualification, creation of a new office or any other
				cause can, upon nomination of the Nominating Committee, be filled by
				the Board for the unexpired portion of the term or for the new term
				established by the Board.</p>
			<h6>7.4 Two Consecutive Meetings.</h6>
			<p>7.4.1 Any member of the Board who misses two (2) consecutive Board
				meetings or five (5) meetings within his or her term, without
				reasonable explanation could have his or her membership on the Board
				reviewed by the Nominating Committee with dismissal possible by a
				vote of the Board.</p>
			<h6>7.5 Compensation.</h6>
			<p>7.5.1 No Director shall receive compensation for service on the
				Board; however, the Board may provide reimbursement for expenses
				incurred by a Director in performance of service to the Board.</p>
		</div>
		<div class="text-center bylawsection">
			<h4>Article VIII</h4>
			<hr width="25%">
			<h5>Committees</h5>
			</br>
			<h6>8.1 Committees.</h6>
			<p>8.1.1. No committee shall have the authority to bind or act on
				behalf of the Board except as may be specifically provided by these
				Bylaws.</p>
			<p>8.1.2. Standing Committees of the Board shall be the Executive
				Committee, Nominating Committee, Budget and Finance Committee,
				Development Committee, and the Communications Committee. The Board
				may also appoint additional committees to further the work of the
				Association.</p>
			<p>8.1.3. Ad hoc committees, such as might be formed to plan for and
				execute special events, can be appointed by the Executive Committee.
			</p>
			<p>8.1.4. Each committee shall consist of two or more Directors.
				Individuals who meet the criteria noted in Article 4, Section 2 for
				Board Membership can also serve on committees as voting members. The
				Executive Committee shall approve members and committee Chairs to
				standing and other committees as recommended by the President.</p>
			<p>8.1.5. The term of a committee Chair and a committee member shall
				be two (2) years. Committee Chairs and members may serve more than
				one term as committee Chairs and members.</p>
			<p>8.1.6. Committees shall meet as necessary to carry out their
				duties.</p>
			<h6>8.2 Executive Committee.</h6>
			<p>8.2.1. Composition, Members of the Executive Committee shall be
				the Past President, President, Vice President, Treasurer, Secretary,
				and the Chairs of the Communications and Development Committees. The
				high school principal or his / her designee currently serving as ex
				officio member of the Board shall serve as an ex officio member of
				the Executive Committee.</p>
			<p>8.2.2. Powers. The Executive Committee shall have the authority of
				the Board of Directors and shall act as the Board of Directors
				between Board meetings, except that the Executive Committee cannot
				authorize distribution of the assets of the Association; approve or
				recommend dissolution of the Association, merger or sale, pledge or
				transfer of all or substantially all of the Association's assets;
				amend the Articles of Incorporation; amend these Bylaws; elect,
				appoint or remove Directors of the Board or members of any
				committee; elect, appoint or remove Officers; approve the annual
				budget; approve the mission statement and long range strategic plan.
				All decisions must be in conformance with adopted Board policies. A
				quorum of two-thirds of the members of the Executive Committee is
				necessary to transact business.</p>
			<p>8.2.3. Duties. The Executive Committee, among other duties, shall
				coordinate the work of the Board and its committees and coordinate
				annual planning and long range strategic planning.</p>
			<p>8.2.4. Reporting. The Executive Committee shall keep and
				distribute to the Board, in a timely manner, minutes of all
				Executive Committee meetings.</p>
			<h6>8.3 Nominating Committee.</h6>
			<p>8.3.1. Composition. Members of the Nominating Committee shall be
				the Past President, President, Secretary, and a minimum of two (2)
				other Board members not presently serving as an Officer or Executive
				Committee member.</p>
			<p>8.3.2. Powers and Duties. The Nominating Committee, among other
				duties, shall: recruit new Directors and nominate new and continuing
				Directors; nominate Officers of the Association; coordinate annual
				evaluation of Board operations; and periodically review these Bylaws
				and recommend revisions to the Board of Directors.</p>
			<h6>8.4 Budget and Finance Committee.</h6>
			<p>8.4.1. Composition. Members of the Budget and Finance Committee
				shall include the President, Treasurer, and such additional members
				as may be recommended by the Executive Committee. The Chair will be
				appointed by the President.</p>
			<p>8.4.2. Powers and Duties. The Budget and Finance Committee shall:
				develop the annual budget and recommend its adoption by the Board;
				monitor the Association's income and expenditures in relation to the
				adopted budget; oversee the Association's financial and tax matters;
				oversee the development of financial and investment policies and
				recommend their adoption by the Board. The Committee shall also
				implement the investment policy after its adoption by the Board;
				select investment managers and allocate funds; review and monitor
				investment manager performance and replace managers when either
				necessary or desirable. The Committee shall report to the Executive
				Committee and Board the results of the investment program at such
				times as it is directed by either to do so.</p>
			<h6>8.5 Development Committee.</h6>
			<p>8.5.1 Composition: Members of the Development Committee shall
				include four members of the board and such additional members as may
				be recommended by the Executive Committee. The Chair will be
				appointed by the President.</p>
			<p>8.5.2 Powers and Duties: The Development Committee shall: be the
				primary coordinating body for activities that promote engagement
				with key stakeholders of the Association and fund raising activities
				of the Association. The Committee or an assigned subcommittee will
				develop and/or support initiatives to actively engage alumni with
				the life of the school, its students, the community and each other
				and work collaboratively with the Communications Committee and other
				committees to further these goals. The Committee or an assigned
				subcommittee will establish fund raising policies, mechanisms,
				priorities and goals - subject to approval by the Board, plan and
				implement fundraising activities to support the goals of the
				Association, and act as a coordinating body for all Association fund
				raising efforts.</p>
			<h6>8.6 Communications Committee.</h6>
			<p>8.6.1 Composition. The Communications Committee shall include
				three (3) members of the full Board, and such additional members as
				may be recommended by the Executive Committee. The Chair of the
				Committee will be annually appointed by the President.</p>
			<p>8.6.2 Powers and Duties. The Communications Committee is
				responsible for all external communications and some internal
				communications for RHSAA. The committee shall develop and oversee an
				infrastructure of communications media that will include but not be
				limited to: RHSAA website; RHSAA blog; RHSAA newsletter and/or
				eNewsletter; RHSAA social media, such as Facebook, Twitter,
				Pinterest, Picasa, YouTube or other internet platforms; press
				releases and or story placement in local publications; outreach to
				news media and/or bloggers when practical. The Communications
				Committee is responsible for developing and managing RHSAA
				messaging, at the direction of the President and the Board. The
				committee shall advise the President and the Board on communications
				strategies and tactics. The committee shall disseminate information
				among committees and/or board members when instructed by the
				President and gather information and/or opinions from board members,
				donors, alumni, school leadership, students, local government and
				others when instructed. The President and/or the President's
				designee shall act as RHSAA spokesperson.</p>
		</div>
