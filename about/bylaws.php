			<h4>Article IX</h4>
			<hr width="25%">
			<h5>Administration and Financial Provisions</h5>
			</br>
			<h6>9.1 Fiscal Year.</h6>
			<p>9.1.1 The fiscal year of the Association shall be July 1 through
				June 30.</p>
			<h6>9.2 Rules of Procedure.</h6>
			<p>9.2.1 The rules contained in the latest revision of Robert's Rules
				of Order shall govern all meetings of the membership and the Board
				and committees, except to the extent that they may be in conflict
				with these Bylaws.</p>
			<h6>9.3 Donations.</h6>
			<p>9.3.1 The Association can accept property by gift, devise or
				bequest for the purposes of the Association.</p>
			<h6>9.4 Investments.</h6>
			<p>9.4.1 The assets of the Association held for investment shall be
				invested pursuant to Section 8.4.</p>
		</div>
		<div class="text-center bylawsection">
			<h4>Article X</h4>
			<hr width="25%">
			<h5>Amendments of Bylaws</h5>
			</br>
			<p>These Bylaws can be amended or repealed by a two-thirds majority
				at a meeting of the Directors of the Board duly called for such
				purpose and at which a quorum is present or by majority of the
				entire Board at any meeting thereof. Written notice of the meeting,
				together with the proposed amendment(s) or repealing language shall
				be mailed to members at least ten (10) calendar days prior to the
				meeting. Upon adoption of an amendment to the Bylaws, the Secretary
				can correct punctuation, grammar, numbering or form where
				appropriate in the Bylaws, if the correction does not change the
				meaning.</p>
		</div>
		<div class="text-center bylawsection">
			<h4>Article XI</h4>
			<hr width="25%">
			<h5>Indemnification</h5>
			</br>
			<p>The Association shall indemnify Officers and Directors made a
				party to a proceeding because the individual is or was an Officer or
				Director against liability incurred in the proceeding if: the
				conduct of the individual was in good faith; the individual
				reasonably believed that the individual's conduct was in the best
				interest of the Association, or at least not opposed to its best
				interest; and in the case of any criminal proceeding, the individual
				had no reasonable cause,such indemnification shall include
				reasonable expenses incurred in the defense of such a proceeding. It
				is the intent of this section to provide indemnification to the
				maximum extent permitted by the Oregon Nonprofit Corporation Law and
				to indemnify each Officer and Director against expenses, judgments,
				fines, settlements, and other amounts actually and reasonably
				incurred in connection with any proceeding arising by reason of the
				fact that such person is or was an Officer or Director of the
				Association.</p>
