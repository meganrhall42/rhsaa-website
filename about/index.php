					<li>The Landscaping Committee will host two campus clean-up days (one in the fall and one in the spring) 
						to beautify the campus grounds, and will invite students, staff, and community members to participate 
						with alumni.</li>
					<li>We will recruit a Scholarship Committee Chair, and offer two $1,000 scholarships to graduating seniors 
						to be awarded in May. The Committee will meet by December 1st, whether a Chair has been established or 
						not.</li>
				</ul>
			</div>
