				<hr width="75%" class="goldbar">
				<ul class="blockDInfo">
					<li>We will offer 5 students $1,000 scholarships (with the intention of aiming for higher if possible). </li>
					<li>Supporting students is a central theme to our organization. We will be donating $5,000 per year to student activities 
						through our grant program.</li>
					<li>One way we can engage more volunteers is to host specific days where alumni can plan and commit to single day activities. By 2024, 
