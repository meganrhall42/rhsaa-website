				Parade in front of 250,000 people and many more on TV.</li>
			<li>Kadel's Auto Body shop produced the RHS motorized football helmet
				which also appeared in local parades.</li>
			<li>Worked with Feeder school development and alignment and support
				services.</li>
			<li>Engaged the various community stakeholders to see RHS as their
				school.</li>
			<li>Helped pass the Bond Measure for the modernization of Roosevelt.</li>
			<li>Tim Nishitani designed a sympathy card and multi-purpose card
				usage, Barb at the ESD provides printing and Melinda Stoneburg
