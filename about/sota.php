		<hr width="25%">
		<ul class="sotaInfo row">
			<li>Established RHSAA as a 501(c)(3) nonprofit association with a
				board of directors, bylaws and standing committees.</li>
			<li>Established a Homecoming Parade and other related Homecoming
				activities including Alumni Dinner Honoring RHS Alums and gathering
				alums from all classes.</li>
			<li>Created a database of more than 2,000 RHS alums.</li>
			<li>Developed and designed Rough Rider support signs to be placed in
				local businesses in partnership with Anderson Signs. The signs were
				sold as a fund raiser.</li>
