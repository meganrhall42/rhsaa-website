					<hr width="75%"
						class="goldbar float-left d-none d-lg-block d-xl-block">
					<p>
						Carolyn Davidson designed the Nike "swoosh" in 1971 while a
						graphic design student at PSU. Phil Knight, who was teaching an
						accounting class at the university, noticed her working on an
						assignment, and asked her to do some work for what was then Blue
						Ribbon Sports, Inc. Needing to choose a logo in order to meet
						looming production deadlines, Knight settled on the swoosh, after
						rejecting various other designs. At the time, he stated of the
						logo, "I don't love it, but it will grow on me." </br>
						</br> For her services, she billed the company $35. The swoosh
						became an instantly-recognizable insignia for Nike around the
						world. Although Davidson left Nike in September 1983, Knight
						invited her to a company lunch. There, he presented her with a
						diamond ring engraved with the swoosh, and an undisclosed amount
						of Nike stock. Of the gift, Davidson says, "this was something
						rather special for Phil to do, because I originally billed him and
						he paid that invoice." </br>
						</br> Davidson retired in 2000, and now engages in hobbies and
						volunteer work, including weekly duties at the Ronald McDonald
						House at Legacy Emanuel Hospital &amp; Health Center.
