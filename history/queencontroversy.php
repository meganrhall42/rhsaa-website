		Over a period of several years, a minor war raged in the Portland newspaper over whether Roosevelt
		had ever had a Rose Festival Queen. The whole thing came to a head on June 8th, 1965, when Sally Swift 
		was named in the headlines as our first queen. The headline of the Oregon Journal for June 9th, 1965
		read: "Sally is SECOND Roosevelt Queen." The accompanying story tells the story of Lucy Lee Thomas, who 
		reighned in 1923.
		<br>
		Briefly, the background is this... in 1923, the fifteenth year of the Rose Festival, a princess was selected from
		each of the city's seven high schools. The girls were Kathryn Roseman of Jefferson, Harriett Dezendorf of Lincoln,
		Wilma Sprague of Commerce (now Cleveland), Pauline Clark of Washington, Gertrude Vessey of Franklin, and Lucy Lee
		Thomas of Roosevelt. The idea behind the selling of votes was to give each citizen of Portland, young and old 
		alike, a chance to really support his choise for queen. Each girl had a campaign manager who was to assist her. 
		The girls themselves did not seel votes, as such activity was not allowed in the high schools. According to 
		newspaper stories of the time, every effort was made to ensure a fair contest, and to encourage a healthy
		rivalry among the schools. 
		<br> 
		Lucy Lee, although she represented the newest and smallest high school in the city, won by a 4,000 vote margin.
		Her total was 136,989. Her closest rival, Gertrude Vessey of Frankline, polled 129,904. Lucy Lee's campaign manager,
		Dean Knowles, was so rattled at the last minute that he forgot to turn in 4,000 votes.
		<br> 
		Ben Hur Lampman was at his most poetic and eloquent in an article written for The Oregonian (unfortunately the date
		does not appear on the clipping):
		<br> 
		The goodle province of St. Johns, a trifle disheveled from the day's occupation, paused yesterday to emit a stenotorian
		shout for Her Majesty, Queen of Rosaria.. Burgher and business man, clear and stevedore, her subjects of St. Johns made
		the well-knownwelkin to ring madly-- for the queen is a daughter of the peninsula, and has brought great honor to 
		her servitors..
		<br> 
		... For once Principal Fletcher, who directs the affairs of Roosevelt High School, found himself outranked by one of
		his own pretty pupils.. called the student body to assembly and told them of the timely and well merited recognition
		that had come to Roosevelt and St. Johns and province...
		<br> 
		... Thereafter the students took the suburb by storm and paraded in honor of her majesty... Last dispatches from the
		province, recieved long after nightfall, were to the effect that St. Johns was still in tumult.
